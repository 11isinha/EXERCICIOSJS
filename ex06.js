// Determine e exiba o resultado das seguintes expressões:

// - `p && q`
// - `p || q`
// - `!p`
// - `!q`

let p = true;
let q = false;
console.log(p && q);
console.log(p || q);
console.log(!p );
console.log(!q);

//  O operador AND (&&) retorna true apenas se ambos forem true. 
// O operador OR (||) retorna true se pelo menos um deles for true. 
// O operador NOT (!) inverte o valor booleano. 
//  !false torna-se true. 