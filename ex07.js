console.log(0 || "Hello");
// Hello
// ||  busca o primeiro valor true (verdadeiro).
console.log(" " && "World");
//Não aparece nada
// &&  busca o primeiro valor false (falso) para retornar.
console.log(null ?? "Default Value");
//Default Value
// ??  só retorna o valor da direita se o valor da esquerda for estritamente null ou undefined.
console.log(undefined ?? 42);
//42
// ??  só retorna o valor da direita se o valor da esquerda for estritamente null ou undefined.


