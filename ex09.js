console.log("5" - 2);
//O resultado será 3, pois 5 - 2 é 3.
console.log("5" + 2);
//O resultado será 52 pois o sinal de mais sozinho não soma, apenas aparece no console.
console.log(true + 1);
//O resultado será 2, pois o true significa 1, e 1 + 1 é 2. 
console.log(false + 10);
//O resultado será 10, pois false significa 0, e 10 + 0 é 10.